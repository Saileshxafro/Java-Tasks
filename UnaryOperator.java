class UnaryOperator{

public static void main(String args[]){



	int a = 5;
	System.out.println(a++);
	System.out.println(a);
	System.out.println(++a);



	int b = 10;
	System.out.println(--b);
	System.out.println(b);
	System.out.println(b--);
	System.out.println(b);



	int c = -10;
	System.out.println(~c);
	System.out.println(!true);
}
}
