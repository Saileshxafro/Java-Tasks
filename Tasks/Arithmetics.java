class Object_1{
		int a = 300;
		int b = 600;
		double c = 20.7899876d;
		double d = 10.9876546d;
	public static void main(String Args[]){
	Object_1 obj = new Object_1();
	System.out.println(obj.a+obj.b);
	System.out.println(obj.b-obj.a);
	System.out.println(obj.a*obj.b);
	System.out.println(obj.b/obj.a);
	System.out.println(obj.c+obj.d);
	System.out.println(obj.c-obj.d);
	System.out.println(obj.c*obj.d);
	System.out.println(obj.c/obj.d);
}
}