class Add{
static byte a = 4;
static byte b = 5;
static float c = 5.678f;
static float d = 2.566f;
	public static void main(String Args[]){
	System.out.println(a+b);
	System.out.println(b-a);
	System.out.println(a*b);
	System.out.println(b/a);
	System.out.println(c+d);
	System.out.println(c-d);
	System.out.println(c*d);
	System.out.println(c/d);
}
}
